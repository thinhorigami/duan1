    public void loadTable() {
        ArrayList<NhanVienn> list = nhanVienService.getAll();
        model = (DefaultTableModel) tbbangNhanVien.getModel();
        model.setColumnCount(0);
        model.addColumn("ID");
        model.addColumn("Mã");
        model.addColumn("Tên");
        model.addColumn("Giới tính");
        model.addColumn("Địa chỉ");
        model.addColumn("Mật khẩu");
        model.addColumn("Điện thoại");
        model.addColumn("Email");
        model.addColumn("Ngày sinh");
        model.addColumn("Chức vụ");
        model.addColumn("Trạng thái");

        model.setRowCount(0);
        for (NhanVienn nhanVienn : list) {
            model.addRow(new Object[]{
                nhanVienn.getId(),
                nhanVienn.getMaNV(),
                nhanVienn.getTenNV(),
                nhanVienn.getGioiTinh(),
                nhanVienn.getDiaChi(),
                nhanVienn.getMatKhau(),
                nhanVienn.getDienThoai(),
                nhanVienn.getEmail(),
                nhanVienn.getNgaySinh(),
                nhanVienn.getIdChucVu(),
                nhanVienn.getTrangThai() == 1 ? "Đang làm" : "Nghỉ làm"
            });

        }

    }

    public void loadCBCChucVu() {
        cbbchucVu.removeAllItems();
        ArrayList<ChucVuu> list = cv.getAll();
        for (ChucVuu i : list) {
            cbbchucVu.addItem(i.getTen());

        }
    }

    public String layIDChucVu(String tenCV) {
        String idCV = null;
        ArrayList<ChucVuu> list = cv.getAll();
        for (ChucVuu chucVu : list) {
            if (chucVu.getTen().equals(tenCV)) {
                idCV = chucVu.getId();
            }

        }
        return idCV;

    }


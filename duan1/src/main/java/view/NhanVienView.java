    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        NhanVienn nv = new NhanVienn();
        nv.setMaNV(txtmaNV.getText());
        nv.setTenNV(txttenNV.getText());
        if (rdonam.isSelected()) {
            nv.setGioiTinh("Nam");
        } else {
            nv.setGioiTinh("Nữ");
        }

        nv.setDiaChi(txtdiaChi.getText());
        nv.setMatKhau(txtmatKhau.getText());
        nv.setDienThoai(txtdienThoai.getText());
        nv.setEmail(txtemail.getText());
        nv.setNgaySinh(txtngaySinh.getText());
        nv.setTrangThai(1);
        String idCV = layIDChucVu((String) cbbchucVu.getSelectedItem());
        nv.setIdChucVu(idCV);

        boolean kt = nhanVienService.add(nv);

        if (kt == true) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadTable();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }

    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:

        NhanVienn nv = new NhanVienn();
        nv.setMaNV(txtmaNV.getText());
        nv.setTenNV(txttenNV.getText());
        if (rdonam.isSelected()) {
            nv.setGioiTinh("Nam");
        } else {
            nv.setGioiTinh("Nữ");
        }

        nv.setDiaChi(txtdiaChi.getText());
        nv.setMatKhau(txtmatKhau.getText());
        nv.setDienThoai(txtdienThoai.getText());
        nv.setEmail(txtemail.getText());
        nv.setNgaySinh(txtngaySinh.getText());

        if (rdodangLam.isSelected()) {
            nv.setTrangThai(1);
        } else {
            nv.setTrangThai(0);
        }
        String idCV = layIDChucVu((String) cbbchucVu.getSelectedItem());
        nv.setIdChucVu(idCV);

        boolean kt = nhanVienService.update(txtmaNV.getText(), nv);

        if (kt == true) {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            loadTable();
        } else {
            JOptionPane.showMessageDialog(this, "Sửa thất bại");
        }
    }//GEN-LAST:event_btnsuaActionPerformed


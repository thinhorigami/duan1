    private void tbbangNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbbangNhanVienMouseClicked
        // TODO add your handling code here:
        int index = tbbangNhanVien.getSelectedRow();
        txtmaNV.setText(tbbangNhanVien.getValueAt(index, 1).toString());
        txttenNV.setText(tbbangNhanVien.getValueAt(index, 2).toString());
        String gioiTinh = tbbangNhanVien.getValueAt(index, 3).toString();
        
        if (gioiTinh.equals("Nam")) {
            rdonam.setSelected(true);
        } else {
            rdonu.setSelected(true);
        }
        

        txtdiaChi.setText(tbbangNhanVien.getValueAt(index, 4).toString());
        txtmatKhau.setText(tbbangNhanVien.getValueAt(index, 5).toString());
        txtdienThoai.setText(tbbangNhanVien.getValueAt(index, 6).toString());
        txtemail.setText(tbbangNhanVien.getValueAt(index, 7).toString());
        txtngaySinh.setText(tbbangNhanVien.getValueAt(index, 8).toString());
        cbbchucVu.setSelectedItem(tbbangNhanVien.getValueAt(index, 9).toString());
        String trangThai = tbbangNhanVien.getValueAt(index, 10).toString();
        if (trangThai.equals("Đang làm")) {
            rdodangLam.setSelected(true);
        } else {
            rdoNghiLam.setSelected(true);
        }
    }//GEN-LAST:event_tbbangNhanVienMouseClicked


